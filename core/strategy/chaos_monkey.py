"""
Chaos-monkey harness (UPGRADE_PLAN Phase U6.6).

A deliberately hostile TEST mode. It takes a clean price history and injects
broker "nastiness" into a COPY of it, then re-scores each registry strategy on
the clean series vs the chaos series and reports which strategies DEGRADE
GRACEFULLY and which SHATTER. Nothing here ever trades, promotes, or edits the
registry - it is a purely offline stress diagnostic that answers one question:

    "If the broker misbehaves for a week, does my edge survive or evaporate?"

Injected nastiness (each independently config-gated, all default OFF so the
whole feature is a no-op until asked for):

  - spread storms   : globally widen the cost model by a multiplier (the
                      backtester already reads spread from config, so this is a
                      config override, not a data edit).
  - requotes        : jitter a fraction of bar OPENS by +/- a few points so
                      next-open fills land at a slightly worse-than-expected
                      price (models a broker requoting the entry).
  - missed bars     : drop a fraction of bars entirely (models a data feed gap
                      / a bar the EA never saw), which also stretches the
                      remaining bars into small gaps.
  - partial fills   : shrink the effective position size on a fraction of trades
                      by scaling the fixed lot down (models the broker filling
                      only part of the requested volume).

The harness is intentionally REPRODUCIBLE: every random choice is driven by a
seed so a chaos verdict can be regenerated byte-for-byte.

Classification
--------------
For each strategy we compute a degradation ratio on net_profit (and require the
chaos run to still be non-catastrophic). A strategy is:

  - GRACEFUL : chaos net_profit stays above `graceful_floor_mult` of its clean
               net_profit (e.g. keeps >= 40% of its edge) AND is still positive.
  - FRAGILE  : keeps some edge but drops below the graceful floor (a warning).
  - SHATTERED: goes non-positive (or catastrophically negative) under chaos.

Pure standard library, ASCII English only, Windows 7 + Python 3.8 friendly.
"""

