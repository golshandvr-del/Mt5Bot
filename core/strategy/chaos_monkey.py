# --------------------------------------------------------------------------- #
# Config
# --------------------------------------------------------------------------- #
class ChaosConfig(object):
    """Parsed, defensively-read view of the ``general.chaos_monkey`` block."""

    def __init__(self, cfg: Any):
        block = {}
        try:
            block = cfg.get_path("general.chaos_monkey", {}) or {}
        except Exception:
            block = {}
        if not hasattr(block, "get"):
            block = {}

        self.enabled = bool(block.get("enabled", False))
        self.seed = self._int(block, "seed", 20240607)
        # Individual nastiness switches (all default off).
        self.spread_storm = bool(block.get("spread_storm", False))
        self.requotes = bool(block.get("requotes", False))
        self.missed_bars = bool(block.get("missed_bars", False))
        self.partial_fills = bool(block.get("partial_fills", False))
        # Intensities.
        self.spread_mult = self._float(block, "spread_mult", 2.0)
        self.requote_frac = self._clamp01(self._float(block, "requote_frac", 0.15))
        self.requote_points = self._float(block, "requote_points", 8.0)
        self.missed_frac = self._clamp01(self._float(block, "missed_frac", 0.05))
        self.partial_frac = self._clamp01(self._float(block, "partial_frac", 0.20))
        self.partial_min_scale = self._clamp01(
            self._float(block, "partial_min_scale", 0.5))
        # Classification thresholds.
        self.graceful_floor_mult = self._float(block, "graceful_floor_mult", 0.4)
        # Chaos net must not fall below this fraction of starting balance to be
        # anything other than SHATTERED (catastrophe guard).
        self.catastrophe_mult = self._float(block, "catastrophe_mult", -0.25)

    @staticmethod
    def _float(block, key, default):
        try:
            return float(block.get(key, default))
        except (TypeError, ValueError):
            return float(default)

    @staticmethod
    def _int(block, key, default):
        try:
            return int(block.get(key, default))
        except (TypeError, ValueError):
            return int(default)

    @staticmethod
    def _clamp01(x):
        try:
            x = float(x)
        except (TypeError, ValueError):
            return 0.0
        if x < 0.0:
            return 0.0
        if x > 1.0:
            return 1.0
        return x

    def any_data_nastiness(self):
        """True if any nastiness that mutates the price series is on."""
        return bool(self.requotes or self.missed_bars)

    def any_nastiness(self):
        return bool(self.spread_storm or self.requotes or self.missed_bars
                    or self.partial_fills)
